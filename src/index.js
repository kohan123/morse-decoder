const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
let res = "";
for (let i = 0; i < expr.length; i = i + 10) {
    const string = expr.slice(i, i + 10);
    if (string[0] === "*") {
        res += " ";
    } else {
        const morseCode = string.replace(/11/g, "-").replace(/10/g, ".").replace(/0/g, "");
        const letter = MORSE_TABLE[morseCode];
        res += letter;
    }
}
    return res;
}

module.exports = {
    decode
}